package com.ibm.example.Log4J2;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;

@WebServlet(urlPatterns="/servlet")
public class HelloServlet extends HttpServlet {
	private static final long serialVersionUID = 8168969542888729995L;
	private static Logger logger = LogManager.getLogger(HelloServlet.class);

	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		logger.info("Hello There!");
		response.getWriter().append("Hello! How are you today?");
    }
}
